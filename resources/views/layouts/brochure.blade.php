            <strong>Chitwan National Park </strong>
        </div>
        <div style="margin-top:20px"></div>
        <div class="para" style="text-align:justify;text-justify:inter-word">
            Chitwan National Park is one of Nepal's 4 World Heritage Sites. It is one of the few remaining undisturbed
            vestiges of the 'Terai' region and is particularly rich in flora and fauna with one of the last populations
            of single-horned Asiatic rhinoceros. It is also one of the last refuges of the Bengal tiger. Activities
            offered in Chitwan include elephant safari, Jeep safari, jungle walk, canoeing, bird watching, village walk
            and cultural tours.
        </div>
